                <p><br>
                    Năm 19 tuổi thì Giáng Tiên xuất giá kết hôn cùng với Trần Đào Lang. Đào Lang là con nuôi một ông
                    quan hưu trí họ Trần ở thôn Vân Đình cũng thuộc xã Tiên Hương. Tục truyền rằng ông quan họ Trần ấy
                    đã nhiều tuổi mà không có con, một hôm ra dạo mát vườn hoa tìm được một cậu bé, ai đem bỏ ở gốc cây
                    đào, bèn đem về nuôi và đặt tên là Đào Lang.<br>
                    Nàng Giáng Tiên về nhà chồng, trên thuận dưới hòa, nức tiếng là người hiền phụ. Đước một năm sinh hạ
                    được một giai đặt tên là Lê Nhâm, năm sau nữa lại sinh thêm được một gái tên Lê Cổn.<br>
                    Sau khi lấy vợ, Đào Lang tỏ vẻ hơi trễ nải việc học hành. Nhân lúc canh khuya dệt vải, nàng Giáng
                    Tiên làm một vài thơ có 28 vị tinh tú để răn đức lang quân. Đào Lang cũng họa một bài có 28 vị sao,
                    và sau khi ấy trở nên rất chăm chỉ.<br>
                    Giữa lúc gia đình đang sống trong cảnh yên vui, đầm ấm, trên có cha mẹ già, dưới có hai con thơ, vui
                    vầy sum họp thì thương ôi nàng Giáng Tiên tự nhiên vô bệnh mà mất. Nàng mất năm 1577 (năm Đinh Sửu),
                    giờ Dần ngày mùng 3 tháng 3, khi ấy mới vừa 21 tuổi, mộ táng ở xứ Cây Đa xã Tiên Hương, hiện nay
                    lăng của ngài vẫn còn ở đấy. Năm 1938, người trong họ Đào chi và những người tín ngưỡng Đức thánh
                    Mẫu ở trong Trung Kỳ đã hưng công đóng góp xây dựng thành một ngôi lăng mộ Đức Thánh Mẫu Phủ Dầy
                    nguy nga tráng lệ.<br>
