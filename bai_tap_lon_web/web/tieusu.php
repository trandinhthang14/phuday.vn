                <strong></strong><br>

                Chép theo ngọc phả thời tiểu sử Đức Thánh Mẫu Phủ Dầy thực là giản dị không có chút gì là huyền bí cả.
                Đức Thánh Mẫu Phủ Dầy giáng sinh đêm trăng rằm tháng 8 năm 1557 (năm Đinh Tỵ – Lê Thiên Hựu đệ nhất tức
                đời vua Lê Anh Tông) tháng Thìn, ngày Dần, giờ Dần. Năm 1577 (năm Đinh Sửu) không bệnh mà mất.<br>
                Thân phụ Ngài là Lê Công Chính và thân mẫu Ngài là Trần Thị Phúc.<br>
                Cao cao Tổ thuộc dòng dõi họ Lê ở trong Thanh Hóa, ở ẩn ra ngoài làng Kẻ Dầy, đổi sang họ Trần, tới đời
                thứ 3 thì lại đổi về họ Lê (Lê Công – Lê Đức Chính). Sau này con cháu mang dòng họ Trần Lê.<br>
                Lê Công đặt tên Ngài là Lê Thị Thắng, tên riêng của Ngài là Giáng Tiên vì ngài đẹp như tiên giáng trần.
                Lê Công là một nhà nho, phong lưu, sung túc, người ở xã Tiên Hương, có đủ vật lực cho con gái theo đòi
                bút nghiêng , thi thư kinh sử, may vá thêu thùa. Nàng Giáng Tiên hồi bấy giờ thật là một người con gái
                hoàn hảo, có nhan sắc thông minh, nết na, lại tài giỏi về nữ công, âm nhạc, thi phú.<br>
                Những lúc nhàn rỗi, Giáng Tiên thường dạo chơi ra vườn gẩy đàn ngâm thơ. Hiện vườn ấy hãy còn di tích,
                lại đăng sau đền thờ Mẫu ở xã Tiên Hương bây giờ.<br>
                <br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/tiêusu.jpg"><br>
