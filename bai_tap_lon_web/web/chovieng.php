            <h1 class="main-content--text">

                <a style="margin-left:450px">
                    Chợ Viềng Nam Định điểm đến hấp dẫn dịp đầu xuân
                </a>

            </h1>
            <div class="medium">
                <br>
                <strong></strong><br>

                Chợ Viềng thuộc thôn Trung Thành, xã Kim Thái, huyện Vụ Bản, Nam Ðịnh, trước đây chợ còn có tên là chợ
                chơi du xuân (du xuân thị). Bao quanh là cả một quần thể di tích thờ Thánh mẫu Liễu Hạnh – Thần chủ Ðạo
                Mẫu Việt Nam, nên cùng với việc đi chơi chợ là hoạt động lễ Mẫu để Người ban cho sức khoẻ và tài lộc.
                Chợ Viềng mỗi năm chỉ họp có đúng một phiên thôi, một phiên chợ kéo dài từ nửa đêm mồng 7 đến sáng mồng
                8 tết.
                <br>

                Lỡ một phiên chợ Viềng là lỡ cả một năm, và cũng có thể là lỡ cả một đời người. Dân Nam Ðịnh đi chợ
                Viềng đầu năm đã đành, người tứ xứ từ Hà Nội vào, Thanh – Nghệ ra, Hải Phòng – Quảng Ninh lên, ai
                cũng cố đi chợ Viềng một lần để mong có lộc cho cả năm buôn may bán đắt; để chơi, để kiếm món đồ có
                giá hời, thậm chí chỉ để lấy tiếng là đã đi chợ Viềng.<br>
                Chợ là hai dãy quán chủ yếu là tre, nứa dựng tạm ở bên lề đường, chạy dài suốt 5km với đầy đủ sắc
                màu. Chợ cũng bày bán đủ mọi thứ hàng hoá, nhưng điều đặc biệt ở đây là cả người bán lẫn người mua
                đều không đặt mục đích lợi ích kinh tế lên trên hết. Ðến chợ Viếng vào xuân, mục đích chính là để
                cầu mong phát tài phát lộc. Ðó chính là lợi ích tinh thần, chỉ cần tham dự chợ là đã được xem là có
                may mắn trong cả năm.<br>
                Tiếng là chợ nhưng ở đây người ta không mua, bán những nhu yếu phẩm cần thiết cho cuộc sống con
                người như gạo, thịt, quần áo, giày dép… càng không phải là các thứ hàng cao cấp, xa xỉ. Chúng đơn
                giản chỉ là những sản phẩm mang tính chất phục vụ sản xuất tiểu nông như cái cày, cái cuốc, cái dao,
                cái liềm, cái thúng, cái mủng… hoặc là một số giống cây trồng, vật nuôi như cây chanh, cây ớt, các
                loại cây cảnh, cây ăn trái… bạn cũng có thể tìm thấy ở đây những bộ tế khí, những chiếc lư hương
                bằng đồng…<br>
                Những người buôn bán ở các tỉnh lân cận như Thái Bình, Hà Nam, Ninh Bình cũng cơm nắm cơm đùm đi từ
                sáng sớm để kịp buổi chợ. Nhữngngười ở tỉnh xa hơn như Thanh Hóa, Lạng Sơn, Hà Giang phải đến từ
                chiều hôm trước tìm nhà nghỉ trọ. Suốt cả vùng từ đêm trước phiên chợ ánh điện chan hoà. Những người
                buôn bán đến Viềng dù xa hay gần, thuận lợi hay cách trở, đến đây mọi người đều mang thứ đặc sản của
                quê hương mình tham gia phiên chợ.<br>
                <br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/cho-vieng.jpg"><br>
                    <i style="margin-left:450px"> </i>
                <p>
                    Chợ Viềng dù chỉ một ngày phiên nhưng hàng hóa thì ngàn vạn, quán ăn hàng quà rải suối ba cây số.
                    Dân trong vùng và xung quanh mang các thứ cần dùng cho đời sống, công việc và học tập của con cháu
                    hàng ngày để mua, bán, trao đổi. Từ các loại cuốc, xẻng đến bát, đĩa, rổ, rá, áo quần, giầy dép cho
                    tới quyển sách, cái bút tồi cây kim, sợi chỉ càng có mặt trong ngày phiên chợ. Trong chợ còn có bán
                    cả các sản phẩm đặc trưng, nổi tiếng của từng vùng như rau cần Thiệu Vịnh, bồ, đó Văn Tập, vó lưới
                    Bồng Làng. Chợ còn l2 nơi bán các loại đồ dùng cũ, ai đến chợ cũng phải mang một vài thứ gì đó, từ
                    một chiếc nồi cũ hay một chiếc cuốc để mòn đều có thể đem bày bán.<br>

                    Bên cạnh những món hàng truyền thống vài chục năm gần đây chợ còn bán cây giống lấy gỗ như bạch đàn,
                    phi lao, xà cừ, cây giống ăn quả như vải thiều, nhãn Hưng Yên, táo Thiện Phiến. Phiên chợ mà người
                    bán không cần bán đắt, người mua chẳng thiết tha lắm với giá rẻ, đắt, chỉ cần ” bán”, ” mua” lấy
                    được cho phát tài quanh năm. Chợ còn là nơi giới thiệu làng nghề, tay nghề ở những vùng quanh đó.
                    Nhiều đôi nam thanh nữ tú tay trong tay vãn cảnh chợ Viềng, cầu mong cho hạnh phúc lứa đôi. Hội chợ
                    Viềng đầu xuân thật vui vẻ nhộn nhịp với tiếng chiêng, trồng hòa lẫn với tiếng gò mâm, gò nồi, hàng
                    búa thợ rèn cùng với âm thanh của hàng ngàn, hàng vạn người mua, kẻ bán, người xem đã tạo nên thứ âm
                    thanh ồn ào, rất đặc trưng của phiên chợ Viềng xuân.<br>
                    Nếu nói đến chợ Viềng mà chỉ nói đến những thứ vừa nêu trên thì chưa thật đầy đủ, vì điểm nổi bật và
                    gây ấn tượng nhất đối với mỗi du khách chính là những miếng thịt bê được thui vàng ruộm. Người ta
                    quan niệm đi chợ Viềng mà không mua bán một thứ gì đó cũng như không có một miếng thịt bê mang về
                    lấy lộc thì coi như chưa đến chợ Viềng, chưa được may mắn. Phải chăng cũng chính vì mang ý nghĩa tâm
                    linh ấy nên hầu như mọi hoạt động trong chợ đều diễn ra theo kiểu “nói sao, mua vậy”. Nghĩa là,
                    người bán không cần nói thách và người mua cũng chẳng phải mặc cả, bởi người xưa cho rằng nếu còn
                    chút gì đó “băn khoăn” về giá cả thì sẽ làm mất hết lòng thành kính và sự tôn nghiêm. Cũng vì mong
                    vận đỏ nên chợ Viềng mới có một đặc sản nữa là thịt bò. Vào phiên chợ Viềng tràn ngập thịt bò. Người
                    ta quan niệm thịt bò mang lại vận son cho cả năm vì có màu đỏ. Một phiên chợ Viềng người ta thui đến
                    vài trăm con bò. Khắp xung quanh khu chợ khói rơm thui bò nghi ngút, đặc quánh như sương. Nhà làm
                    hàng thịt mổ bò đã đành, nhà bình thường cũng đi mua vài con bò về mổ, chất rơm thui và bày bán như
                    ai. Suốt một ngày một đêm chợ ngây ngất mùi thịt bò với hai món “bá chủ” là phở bò và thịt bò xào.
                    Ai không ăn được ráng chịu vì không còn thứ gì khác.<br>
                    Muốn đi được cả hai chợ Viềng thì phải từ Viềng Phủ ra đi vào giữa đêm. Trời tối như mực. Ðường sang
                    Viềng Nam Trực phải đi qua hai quãng đồng rộng mênh mông, gió thổi ù ù lạnh buốt. Viềng Nam Trực
                    cách Viềng Phủ 30km nhưng dân chơi đồ cổ đã chực sẵn ở đó từ chập tối hôm trước. Viềng Nam Trực còn
                    được gọi là Viềng Chùa vì chợ phiên nằm sát chùa Ðại Bi, thậm chí cái đuôi chợ còn ăn lấn cả vào sân
                    chùa và cái đuôi ấy cũng chính là phần hấp dẫn nhất của Viềng Chùa: đồ cổ thứ thiệt nằm lẫn trong vô
                    số đồ gia dụng cũ kỹ.
