                lượng sắc Phong lớn nhất Việt Nam dưới các triều Trần, Lê, Nguyễn sắc phong đến 28 lần (Trần triều 7
                đạo, Lê triều 13 đạo, Nguyễn triều 8 đạo) – một kỷ lục về sắc phong, khó có đền phủ nào được như vậy. Số
                lượng sắc phong này ngang với Phủ Chính Tiên Hương – Nơi chính thờ Thánh Mẫu Liễu Hạnh tại Phủ Dầy (Nam
