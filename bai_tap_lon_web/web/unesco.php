                Đón Bằng công nhận danh hiệu Di sản văn hóa phi vật thể đại diện của nhân loại của UNESCO cho Thực hành
                tín ngưỡng Thờ Mẫu tam phủ của người Việt đã diễn ra tại Quần thể di tích Phủ Dầy, Nam Định ngày
                2/4/2017. Tham dự buổi lễ có nguyên Chủ tịch Quốc hội Nguyễn Văn An, Phó Thủ tướng Chính phủ Vũ Đức Đam,
                cùng lãnh đạo các Bộ, ngành Trung ương, địa phương cùng đông đảo nhân dân địa phương.
                Phát biểu tại buổi lễ, bà Susan Vize - quyền Trưởng đại diện Văn phòng UNESCO Hà Nội cho hay, Nam Định
                là một trong những trung tâm của "Thực hành tín ngưỡng Thờ Mẫu tam phủ của người Việt”, từ chỗ bị hạn
                chế do hiểu nhầm, cấm đoán, những giá trị của di sản này đã được hồi sinh và phát triển mạnh mẽ. Có được
                điều này là nhờ sự cố gắng của Chính phủ Việt Nam và đặc biệt là niềm tin và quyết tâm của những người
                tham gia Thực hành di sản này.
                Theo bà Susan Vize, việc UNESCO ghi danh một di sản không phải là điểm kết thúc mà là khởi đầu cho một
                hành trình mới. Bởi để có được sự bền vững trong công tác bảo vệ và phát huy di sản chưa bao giờ là công
                việc dễ dàng, vì vừa phải bảo vệ giữ gìn các giá trị truyền thống vừa phải tiếp thu các giá trị hiện
                đại. Hơn nữa, điều tối quan trọng là phải thu hút được sự tham gia của các bên liên quan, đó là cộng
                đồng nắm giữ và thực hành di sản, con cháu của họ phải là trung tâm của việc gìn giữ và bảo vệ này.
