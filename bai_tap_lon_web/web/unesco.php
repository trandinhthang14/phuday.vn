                <p>
                    <img style="margin-left:320px" src="./anh/traobang.jpg"><br>
                    <i style="margin-left:580px" >1. Lễ Trao Bằng Công Nhận Di Sản</i>
                <p><br>
                Quyền Trưởng đại diện Văn phòng UNESCO Hà Nội cũng bày tỏ mong muốn Chính phủ Việt Nam tiếp tục đồng
                hành cùng các thành viên "Thực hành tín ngưỡng Thờ Mẫu tam phủ của người Việt" và có những chính sách
                bảo vệ di sản hữu hiệu, đồng thời cần không ngừng nâng cao nhận thức cho công chúng và thế hệ tương lai
                về các giá trị của di sản, đặc biệt là lòng bác ái và sự khoan dung.
                Thay mặt lãnh đạo Đảng và Nhà nước chúc mừng sự kiện Thực hành tín ngưỡng Thờ Mẫu tam phủ của người Việt
                được UNESCO vinh danh, Phó Thủ tướng Vũ Đức Đam khẳng định, cộng đồng các địa phương là chủ thể của di
                sản đã sáng tạo, gìn giữ, phát huy giá trị của di sản góp phần xây dựng nền văn hóa Việt Nam đa dạng
                trong sự thống nhất.
                Phó Thủ tướng cho biết, “Thực hành Tín ngưỡng thờ Mẫu tam phủ của người Việt” được ghi danh là Di sản
                văn hóa phi vật thể đại diện của nhân loại đã khẳng định những giá trị mang tính toàn cầu của di sản, đó
                là đóng góp của dân tộc ta vào sự phong phú, đa dạng văn hóa của nhân loại, phản ánh sự chủ động, hội
                nhập tích cực của đất nước ta với quốc tế. Đồng thời cũng đặt ra trách nhiệm của Đảng và Nhà nước ta, sự
                chung tay của cộng đồng trong công tác bảo vệ, phát huy giá trị của di sản, thực hiện nghiêm túc cam kết
                quốc tế.
                Vì vậy, Phó Thủ tướng đề nghị, sau sự kiện này, Bộ Văn hóa, Thể thao và Du lịch, Ủy ban quốc gia UNESCO
                Việt Nam tiếp tục hợp tác chặt chẽ với tỉnh Nam Định và các địa phương có di sản này sớm xây dựng Đề án
                bảo vệ và phát huy giá trị di sản để “Thực hành Tín ngưỡng thờ Mẫu tam phủ của người Việt” cùng các di
                sản văn hóa phi vật thể khác của Việt Nam đã được cộng đồng quốc tế vinh danh luôn được tỏa sáng; góp
                phần xây dựng và phát triển nền văn hóa Việt Nam tiên tiến đậm đà bản sắc dân tộc, tạo sức mạnh nội sinh
                quan trọng, đóng góp vào sự phát triển bền vững và bảo vệ vững chắc Tổ quốc vì mục tiêu dân giàu, nước
                mạnh, dân chủ, công bằng, văn minh.
                Cũng tại buổi lễ, Bộ trưởng Bộ Văn hóa, Thể thao và Du lịch Nguyễn Ngọc Thiện đã công bố chương trình
                hành động nhằm bảo tồn, phát huy bền vững di sản "Thực hành tín ngưỡng Thờ Mẫu tam phủ của người Việt".
                Đồng thời cam kết sẽ chỉ đạo, đồng hành với các địa phương thực hiện tốt việc bảo tồn, phát huy Di sản
                này, và sớm xây dựng Đề án bảo vệ và phát huy giá trị di sản “Thực hành tín ngưỡng thờ Mẫu tam phủ của
                người Việt”.
