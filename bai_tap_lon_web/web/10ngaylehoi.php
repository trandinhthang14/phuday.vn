            </h1>
            <div class="medium">
                Trong bài viết này, xin trích lục nội dung kể về mười ngày lễ hội Phủ Dầy trong cuốn “Sự tích
                hội Phủ-Giầy : Sự tích Đức Liễu Hạnh công chúa” do quan tri huyện Nguyễn Quan Phúc cho biên soạn, in
                xuất bản năm 1942.
                <br>
                <strong> Từ mồng 1 đến mồng 10 tháng ba An Nam,</strong><br>

                “Phủ Dầy là địa danh ở xã Tiên Hương, tổng Đồng Đội, huyện Vụ Bản, tỉnh Nam Định, ô tô đi từ tỉnh Nam
                Định vào đến tận Phủ chừng 14 km. Từ Phủ ra huyện lỵ Vụ Bản phải đi 3 km.
                Nhiều du khách Tây Nam nghe tin có hàng vạn người đi trẩy hội Phủ Dầy cũng nô nức rủ nhau đến thăm Phủ.
                Tưởng chừng như sẽ được đi du ngoạn ở một nơi danh lam thắng cảnh nào, như ở Chùa Hương Tích hay chùa
                Yên Tử chăng, nhưng ai nấy đều bị thất vọng cả, Phủ Dầy chỉ có một ngôi đền nhỏ bán cổ bán kim, cũng như
                trăm nghìn ngôi đền khác. Chung quanh có nhiều ngôi đền phụ thuộc ở rải rác khắp mọi nơi. Trước mặt đền
                có một dải núi đất chắn ngang trông như một chiếc bình phong che phủ. Những ngày hội đứng trên dãy núi
                đất ấy trống xuống đền, thấy những khách thập phương chen vai thích cách nhau trên những con đường vào
                Phủ, trông không khác những đảm kiến cỏ vậy. CÁc bạn nào muốn biết sự đông vui của ngày hội ấy đến bậc
                nào, thì nên đi trẩy hội vào những ngày mồng 3, mồng 6 và mồng 7 tháng 3 an nam.
                <br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/10ngay1.jpg"><br>
                    <i style="margin-left:450px">1. Quang cảnh hội Phủ Dầy nhìn từ trên cao(1920-1929) </i>
                <p><br>
                    <strong>Mồng 3 tháng ba</strong><br>
                    Ngày này là ngày Quốc tế, xuốt ngày chỉ có tế, hàng tỉnh tế, hàng huyện và hàng tổng , hàng xã tế,
                    rồi tới hàng thôn, hàng giáp tế, họ nhà Thánh tế, nhà binh tế.<br>
                    Khi hàng tỉnh tế có quan tỉnh đại diện cho quan Tổng đốc vào chủ tế.<br>
                    Khi hàng huyện tế thì Quan huyện Vụ Bản cùng các Chánh Phó Tổng vào tế mà quan huyện làm chủ tế.
                    <br>
                    <br>
                    <strong>Mồng 6 tháng ba</strong><br>
                    Vào buổi trưa có một đám rước thật lớn, hàng vạn người tham dự mà 90 phần trăm là phụ nữ, rước bát
                    nhang Đức Thánh Mẫu Phủ Dầy từ Phủ Chính xã Tiên Hương lên chùa Gôi. Chùa Gôi ở trên một ngọn núi
                    đất cao cận ngay đường xe lửa Nam Định đi Ninh Bình, cách tỉnh lỵ Nam Định 14 km. Bạn đi xe hỏa
                    xuống ga Gôi thì đến nơi này. Muốn xem rõ đám rước ấy xin mời bạn lên hẳn trên núi đứng mà trông
                    xuống.
                    <br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/10ngay2.jpg"><br>
                    <i style="margin-left:480px">2. Rước bát nhang Đức Thánh Mẫu Phủ Dầy (1920-1929)</i>
                <p><br>
                    Đám rước cũng chỉ như các đám rước ở mọi đình chùa khác mà thôi, có thật nhiều tàn quạt, cờ, kiệu,
                    võng lọng, có rước sư tử, có rước rồng, có bát âm, trống phách. Duy có một điều đặc sắc là đám rước
                    này hình như chỉ để dành riêng cho các bà, các cô, dù cũng có ít nhiều ông đồng siên lình len lỏi
                    một cách vô lý vào đấy. Các bà các cô nào đã có đồng thì đến mồng 6 tháng ba, đều coi như bổn phận
                    phải có mặt ở đám rước ấy vậy. Các cô còn trẻ tuổi được cử vào khiêng long đình, rước võng, che tàn,
                    che quạt, các bà mỗi người đều dùng vào một việc trong đám rước, cầm phướn, cầm cờ, dẹp đường ở chỗ
                    múa kiệu vv…v.. Bà nào cô nào cũng đều ăn mặc sặc sỡ, khoe hồng, khoe tía.
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/10ngay3.jpg"><br>
                    <i style="margin-left:480px">3. Rước bát nhang Đức Thánh Mẫu Phủ Dầy (1920-1929) </i>
                <p><br>
                    Đứng trên cao trông xuống đám rước đi dài tới hơn cả một cây số, thấy đủ các mầu xanh xanh, đỏ đỏ ,
                    vàng vàng, hai bên đường các dòng người xem rước chen chúc nhau, bám đen vào những sườn núi đất màu
                    nâu, ở giữa cánh đồng xanh bát ngát<br>
                    <strong>Mồng 7 tháng ba, Hội kéo chữ</strong><br>
                    Theo tục đã có từ lâu, cứ đến ngày này, mỗi tổng huyện Vụ Bản, các viên tổng lý cắt 100 phu cờ tuổi
                    từ 20 đến 35 tuổi, cả huyện Vụ Bản có mười tổng thì cắt ra 1000 phu cờ. Ngoài ra còn 2 tổng La Xá và
                    Vụ Bản trước thuộc về huyện Vụ Bản, sau nhà nước cắt tổng La Xá cho sáp nhập về huyện Mỹ Lộc, mà
                    tổng Vụ Bản sát nhập phủ Bình Lục, hai tổng La Xá và Vụ Bản cũng vẫn nhớ tục cũ mà cứ đến niên lại
                    cắt mỗi tổng thêm 100 phu cờ góp vào cùng 1000 phu ở huyện Vụ Bản để dự hội kéo chữ ở Phủ Dầy.<br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/10ngay4.jpg"><br>
                    <i style="margin-left:580px">4. Hội kéo chữ (1920-1929) </i>
                <p><br>
                    Phu cờ ăn mặc giống nhau, khăn đen phủ một miếng vải mầu cá vàng, áo cánh màu cá vàng và quần trắng,
                    mỗi người tay cầm một chiếc gậy to, làm bằng một cây luồng thẳng dài 4,5 thước tây, đầu buộc một cái
                    chổi lông gà, quanh gậy có dán giấy và buộc từng múi giấy ngũ sắc vào các đầu mấu cây luồng cho có
                    vẻ mỹ quan.
                    Dưới quyền chỉ huy của một người tổng cờ, các phu cờ họp nhau ở một nơi định trước rồi vác gậy đi
                    đều hàng đôi qua một quãng đường dài để đến miếng đất rộng ở trước Phương Đình Phủ Chính. Đến đấy
                    các phu cờ chia nhau ra đóng thành bốn điếm đợi giờ kéo chữ.<br>
                    Kéo chữ thao cách như sau này:<br>
                    Chừng quá trưa một tí, người tổng cờ nổi một hiệu trống để báo cho các phu cờ cùng các khách quan
                    biết là sắp ngả chữ.
                    Muốn được dễ hiễu, ta ví dụ: Tổng cờ phải xếp người thành hình chữ tam (). Giữa những tiếng trống
                    phách inh ỏi, tổng cờ xếp cho một toán phu cờ đứng thành hàng chữ nhất () quay mặt vào đền. Lại xếm
                    một toán nữa đứng sau toán thứ nhất, nhưng ngắn hơn toán thứ nhất một chút, rồi xếp toán thứ ba đứng
                    sau toán thứ nhì và dài hơn toán thứ nhì. Xong đâu đấy im tiếng trống, tổng cờ bèn hô một tiếng to,
                    gõ vào tang trống cách thật mạnh làm hiệu. Các phu cờ bèn vứt ngả các cây gậy xuống rồi ngồi xổm cả
                    trên mặt đất. Đứng ở trên Phương Đình trông xuống thấy những toán khăn đen, áo mầu cá vàng, xếp
                    thành hình chữ tam ở giữa những đám gậy có cán giấy xanh xanh, đỏ đỏ, để ngổn ngang trên mặt đất.<br>
                    Từ mồng một đến mồng 10 tháng ba an nam, suốt trong mười ngày hội, ở trong đền, đêm ngày bất cứ giờ
                    nào cũng có ít ra 5,6 các cụ, các bà hầu bóng trong cùng một lúc. Có bà phải thức thâu đêm suốt
                    sáng, mang ngay ra cạnh chỗ ngồi đủ khăn chầu áo ngự để chờ đến lượt mình ngồi hầu bóng. Các cung
                    văn có đến cả vài chục người.Xuốt ngày đêm tiếng trống, tiếng thanh la, tiếng đàn, tiếng hát ầm ầm ỷ
                    ỷ không nghe rõ thành một thứ tiếng gì. Rồi trong khi giữa các làn khói trầm hương nghi ngút, các
                    giá Cô, cả giá Cậu nữa, truyền phán, ban phước và dậy bảo điều lành, thì các bà đi hầu giá bên cạnh,
                    kêu van, khấn vái nhỏ to. Những người đứng xem chung quanh cười nói, chỉ trỏ, trò truyện, diễn thành
                    một cảnh tượng náo nhiệt, tưng bừng, lộn xộn trong bầu không khí nặng nề khó tả.


