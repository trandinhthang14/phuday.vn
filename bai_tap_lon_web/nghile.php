            <h1 class="main-content--text">

                <a style="margin-left:550px">
                    Nghi lễ tôn nhang bản mệnh (đội bát nhang)
                </a>

            </h1>
            <div class="medium">
                <br>
                Trong tín ngưỡng thờ Mẫu Tam Tứ Phủ của người Việt có rất nhiều nghi lễ, trong đó có nghi lễ Tôn nhang
                bản mệnh phụng sự chư vị cai quản bản mệnh lục thập hoa giáp (60 năm theo can chi) tại các đền, phủ,
                điện, đài thờ Tứ phủ gọi tắt là “tôn nhang bản mệnh” (hay đội bát nhang).<br>
                <strong>Tôn nhang bản mệnh là gì ?</strong><br>
                Tôn nhang bản mệnh là nghi lễ công nhận một người chính thức trở thành đệ tử của Tín ngưỡng thờ Mẫu Tứ
                phủ. Khi bạn tôn nhang bản mệnh tức là bạn thành tâm gửi thân mệnh của mình nơi Tiên Thánh. Nhờ sự gia
                ân, che chở độ trì của Tiên Thánh cho cuộc sống được bình yên và may mắn, vạn sự hanh thông, tài lộc
                vượng tiến.<br>
                <strong>Tôn nhang bản mệnh thực hiện vào thời gian nào ?</strong><br>
                Thời gian thực hiện cho nghi lễ Tôn nhang bản mệnh thường là mùa Xuân (tháng giêng, hai, ba) hoặc mùa
                Thu (tháng tám, chín, mười) trước khi lập đông. Lưu ý không thực hiện tôn nhang vào dịp tháng bảy mặc dù
                là tiết Thu, vì tháng bảy là tháng ngâu buồn tẻ không vượng khí cho tín chủ.<br>
                <br>
                <p>
                    <img style="margin-left:320px;width:665px;height:431px" , src="./anh/ton-nhang-ban-menh.jpg"><br>
                    <i style="margin-left:450px"> </i>
                <p><br>
                    <strong>Tôn nhang bản mệnh thực hiện ở đâu ?</strong><br>
                    Nghi lễ Tôn nhang bản mệnh được thực hiện tại các đền, phủ, điện, đài thờ Tứ phủ, tuyệt đối không
                    thực hiện nghi này này ở nơi thờ Tam Bảo, chư Phật, đình, miếu thờ Thành Hoàng hoặc các vị nhân thần
                    không có trong Tứ phủ.<br>
                    Khi tôn nhang bản mệnh, cần thiết có sự hướng dẫn, giúp đỡ của thủ nhang, hay đồng trưởng của đền,
                    điện hoặc đồng thầy nơi khác đến.<br>
                    <strong>Chuẩn bị cho nghi lễ Tôn nhang bản mệnh</strong><br>
                    Để tiến hành nghi lễ tôn nhang bản mệnh, tín chủ cần phải chuẩn bị lễ vật cúng Thánh trong ngày hôm
                    đó. Lễ vật có thể bao gồm:<br>
                    Lễ lục cúng (hương-đăng-hoa-trà-quả-thực) tùy tâm theo điều kiện và lễ mặn (gà hoặc miếng thịt lợn
                    luộc, đĩa xôi, rượu).<br>
                    Lễ cúng hạ ban là 7 quả trứng gà hoặc trứng vịt sống, gạo, muối, rượu và có thể thêm miếng thịt lợn
                    sống xắt ra 5 miếng nhỏ.<br>
                    Vàng mã có nghìn vàng Tứ phủ hoặc nghìn vàng hoa, mâm hài Tứ Phủ 24 đôi (12 đôi to và 12 đôi nhỏ
                    chia 4 màu), thêm mấy đinh vàng lá … (có bảng tra bản mệnh lục thập hoa giáp in tên hiệu Thánh bản
                    mệnh và tiến lễ kim ngân hài hán, vàng mã và đồ vật tùy tuổi của tín chủ).<br>
                    <strong>Tiến hành nghi lễ Tôn nhang bản mệnh</strong><br>
                    Khi tôn nhang bản mệnh, tín chủ được ngồi ở giữa sập hành lễ, trùm khăn phủ diện đỏ trên đội tráp
                    hay mâm có bát nhanh, sớ xin tôn nhang, vàng lá, đôi nến đặt hai bên bát nhang, hoa tươi, quả cau,
                    lá trầu. Đồng thầy đại tấu thỉnh tên hiệu Thánh bản mệnh cho tín chủ, đọc tên tuổi, địa chỉ tín chủ
                    với các điều cầu mong cát khánh. Kêu cầu Phật thánh gia hộ tín chủ, khất đài được nhất âm nhất dương
                    mới kêu xin hạ bát nhang xuống để yên vị tại đền, phủ.<br>

                    Sau khi đã yên vị bát nhang thì xin hóa kim ngân vàng mã, hài hán, giấy sớ và sau ba ngày thì phải
                    đến đền thành tâm lễ tạ Tiên Thánh. Từ đây tín chủ đã chính thức trở thành đệ tử của Tín ngưỡng thờ
                    Mẫu, nơi tôn nhang bản mệnh được coi là chốn tổ của đệ tử ấy.<br>

                    Sau này một năm bốn tiết (thượng nguyên, tất niên, nhập hạ, tán hạ), hoặc một tháng đôi tuần ngày
                    rằm mùng một, đệ tử thành tâm đến chốn tổ để tạ ơn Tiên Thánh. Nếu có điều kiện thì tham gia các
                    hoạt động của bản hội nơi chốn tổ để tăng tình đoàn kết, giúp đỡ lẫn nhau thông qua các dịp hành
                    hương lễ Thánh, các hoạt động từ thiện.<br>
